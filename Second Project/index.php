    <p>Mustafa Kemal Atatürk, geboren 1881 in Thessaloniki (damals Teil des Osmanischen Reiches, heute Griechenland), war der Gründer der modernen Republik Türkei und ihr erster Präsident. Nach dem Zusammenbruch des Osmanischen Reiches nach dem Ersten Weltkrieg führte Atatürk die türkische Nationalbewegung an, die im Unabhängigkeitskrieg von 1919 bis 1923 gegen die Besatzungsmächte kämpfte. 1923 wurde die Republik Türkei ausgerufen, und Atatürk führte umfassende Reformen durch, um das Land zu modernisieren und zu säkularisieren.

Seine Reformen zielten darauf ab, die Türkei von einem religiös geprägten Sultanat in eine moderne, westlich orientierte Nation zu transformieren. Dazu gehörten die Abschaffung des Kalifats, die Einführung eines neuen Zivilrechts nach europäischem Vorbild und die Trennung von Staat und Religion. Außerdem förderte er die Bildung und die Gleichberechtigung der Frauen, führte das lateinische Alphabet ein und stärkte die Wirtschaft.

Atatürk wird in der Türkei als Nationalheld verehrt. Sein Name "Atatürk" bedeutet "Vater der Türken" und wurde ihm 1934 vom Parlament verliehen. Bis zu seinem Tod 1938 prägte er die Politik und das gesellschaftliche Leben des Landes. Atatürks Vision von einem modernen, laizistischen und fortschrittlichen Staat prägt die Türkei bis heute. <p>


